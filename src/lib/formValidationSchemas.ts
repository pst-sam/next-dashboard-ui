import { z } from "zod";

export const subjectSchema = z.object({
  id: z.coerce.number().optional(),
  name: z.string().min(1, { message: "Subject name is required!" }),
  classId: z.array(z.coerce.number()), //class ids
  teachers: z.array(z.string()), //teacher ids
  
});

export type SubjectSchema = z.infer<typeof subjectSchema>;

export const classSchema = z.object({
  id: z.coerce.number().optional(),
  code:  z.string().min(1, { message: "Class code is required!" }),
  classOrder: z.coerce.number().min(1, { message: "Class order is required!" }),
  arm: z.coerce.number().optional(),
  name: z.string().min(1, { message: "Class name is required!" }),
  academicSession: z.enum(["2024/2025"], { message: "Session is required!" }),
  term: z.enum(["1st", "2nd", "3rd"], { message: "Term is required!" }),
  batchStartDate: z.coerce.date({ message: "Start Date is required!" }),
  batchEndDate: z.coerce.date({ message: "End Date is required!" }),
  capacity: z.coerce.number().min(1, { message: "Capacity is required!" }),
  //gradeId: z.coerce.number().min(1, { message: "Grade name is required!" }),
  students: z.array(z.string()).optional(), // student ids
  supervisorId: z.coerce.string().optional(),
  
});

export type ClassSchema = z.infer<typeof classSchema>;

export const teacherSchema = z.object({
  id: z.string().optional(),
  username: z
    .string()
    .min(3, { message: "Username must be at least 3 characters long!" })
    .max(20, { message: "Username must be at most 20 characters long!" }),
  password: z
    .string()
    .min(8, { message: "Password must be at least 8 characters long!" })
    .optional()
    .or(z.literal("")),
  name: z.string().min(1, { message: "First name is required!" }),
  surname: z.string().min(1, { message: "Last name is required!" }),
  email: z
    .string()
    .email({ message: "Invalid email address!" })
    .optional()
    .or(z.literal("")),
  phone: z.string().optional(),
  address: z.string(),
  img: z.string().optional(),
  bloodType: z.string().min(1, { message: "Blood Type is required!" }),
  birthday: z.coerce.date({ message: "Birthday is required!" }),
  sex: z.enum(["MALE", "FEMALE"], { message: "Sex is required!" }),
  subjects: z.array(z.string()).optional(), // subject ids
});

export type TeacherSchema = z.infer<typeof teacherSchema>;
